import mongoose from "mongoose";

const contactWishlenSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },

    email: {
      type: String,
      required: true,
      trim: true,
      lowercase: true,
    },

    message: {
      type: String,
      required: true,
      trim: true,
    },
  },
  {
    timestamps: true,
  }
);

export const ContactWishlen = mongoose.model(
  "ContactWishlen",
  contactWishlenSchema
);